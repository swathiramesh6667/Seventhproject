package com.conditionalLoops;


public class Task4 
{

	public static void main(String[] args) 
	{
		
		/**
		 * 
		 * initialization;
		 * 
		 * while(condition
		 * {
		 * 		//print statement
		 * 		//increment/decrement statement
		 * }
		 * 
		 */

		//initialization;
		int a = 1;

		//condition
		while(a <= 5)
		{
			System.out.println(a);
			a++;
		}

	}

}
