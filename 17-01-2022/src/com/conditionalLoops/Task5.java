package com.conditionalLoops;


public class Task5 
{

	public static void main(String[] args) 
	{
		//do-while loop
		{
			/**
			 * 
			 * initialization;
			 * 
			 * do
			 * {
			 * 		//print statement
			 * 		//increment/decrement statement
			 * }
			 * while(condition);
			 * 
			 */

			//initialization;
			int a = 1;

			//condition
			do
			{
				System.out.println(a);
				a++;
			}
			while(a <= -5);
			
		
	}
	}
}
